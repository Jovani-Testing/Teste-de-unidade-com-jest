const somar = (a, b) => {
  return a + b;
};

const diminuir = (a, b) => {
  return a - b;
};

const multiplicar = (a, b) => {
  return a * b;
};

const dividir = (a, b) => {
  return a / b;
};

const somar_Multiplicar = (a, b) => {
  return a + b *2 ;
};

const somar_somarb = (a, b) => {
  return a + b + b ;
};

module.exports = {
  somar,
  diminuir,
  multiplicar,
  dividir,
  somar_Multiplicar,
  somar_somarb
};
